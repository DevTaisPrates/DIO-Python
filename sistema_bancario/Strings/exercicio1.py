mensagem = 'Alguns métodos de classe str.'

print(mensagem.upper()) # Os caracteres da string fica maiúsculo.
print(mensagem.lower()) # Os caracteres da string fica minúsculo.
print(mensagem.title()) # Apenas o primeiro caractere da string fica maiúsculo, os demais ficam minúsculo.
print(mensagem.format()) # Formata valores especificados em uma cadeia de caracteres
print(mensagem.isalnum())  # Retorna True se todos os caracteres na string forem alfanuméricos
print(mensagem.isalpha()) # Retorna True se todos os caracteres da cadeia de caracteres estiverem no alfabeto
print(mensagem.isdecimal()) # Retorna True se todos os caracteres na string forem decimais
